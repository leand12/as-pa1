        suspendButton.setEnabled(true);
        stopButton.setEnabled(true);
        NoA.setEnabled(false);
        NoC.setEnabled(false);
        NoS.setEnabled(false);
        PT.setEnabled(false);
        ET.setEnabled(false);
        MAT.setEnabled(false);
